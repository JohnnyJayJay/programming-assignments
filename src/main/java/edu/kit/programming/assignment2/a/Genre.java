package edu.kit.programming.assignment2.a;

/**
 * An enum representing musical genres.
 *
 * @author Jana, Johnny, Christof
 * @version 1.0
 */
public enum Genre {
    /**
     * Rock music is a broad genre of popular music that originated as "rock and roll"
     * in the United States in the late 1940s and early 1950s, developing into a range
     * of different styles in the mid-1960s and later, particularly in the United States
     * and the United Kingdom. It has its roots in 1940s and 1950s rock and roll,
     * a style which drew heavily from the genres of blues, rhythm and blues, and from
     * country music. Rock music also drew strongly from a number of other genres such
     * as electric blues and folk, and incorporated influences from jazz, classical,
     * and other musical styles. For instrumentation, rock has centered on the
     * electric guitar, usually as part of a rock group with electric bass, drums,
     * and one or more singers. Usually, rock is song-based music with a 4
     * 4 time signature using a verse–chorus form, but the genre
     * has become extremely diverse. Like pop music, lyrics often stress romantic
     * love but also address a wide variety of other themes that are frequently social
     * or political.
     *
     * @see <a href="https://en.wikipedia.org/wiki/Rock_music">Wikipedia</a>
     */
    ROCK,
    /**
     * Pop is a genre of popular music that originated in its modern form during the
     * mid-1950s in the United States and the United Kingdom. The terms popular music
     * and pop music are often used interchangeably, although the former describes
     * all music that is popular and includes many disparate styles. During the 1950s
     * and 1960s, pop encompassed rock and roll and the youth-oriented styles
     * it influenced. Rock and pop remained roughly synonymous until the late 1960s,
     * after which pop became associated with music that was more commercial, ephemeral,
     * and accessible.
     *
     * @see <a href="https://en.wikipedia.org/wiki/Pop_music">Wikipedia</a>
     */
    POP,
    /**
     * Hip hop music, also known as rap music, is a genre of popular music developed
     * in the United States by inner-city African Americans and Latino Americans
     * in the Bronx borough of New York City in the 1970s. It consists of a stylized
     * rhythmic music that commonly accompanies rapping, a rhythmic and rhyming speech
     * that is chanted. It developed as part of hip hop culture, a subculture defined
     * by four key stylistic elements: MCing/rapping, DJing/scratching with turntables,
     * break dancing, graffiti writing. Other elements include sampling beats
     * or bass lines from records (or synthesized beats and sounds), and rhythmic
     * beatboxing. While often used to refer solely to rapping, "hip hop" refers to the
     * practice of the entire subculture. The term hip hop music is sometimes
     * used synonymously with the term rap music, though rapping is not a required
     * component of hip hop music; the genre may also incorporate other elements of
     * hip hop culture, including DJing, turntablism, scratching, beatboxing, and
     * instrumental tracks.
     *
     * @see <a href="https://en.wikipedia.org/wiki/Hip_hop_music">Wikipedia</a>
     */
    HIP_HOP,
    /**
     * Punk rock (or simply punk) is a music genre that emerged in the mid-1970s.
     * Rooted in 1960s garage rock, punk bands rejected the perceived excesses of
     * mainstream 1970s rock. They typically produced short, fast-paced songs with
     * hard-edged melodies and singing styles, stripped-down instrumentation, and
     * often political, anti-establishment lyrics. Punk embraces a DIY ethic;
     * many bands self-produce recordings and distribute them through independent
     * record labels.
     *
     * @see <a href="https://en.wikipedia.org/wiki/Punk_rock">Wikipedia</a>
     */
    PUNK,
    /**
     * Schlager music (German: [ˈʃlaːɡɐ], "hit(s)") is a style of European popular
     * music that is generally a catchy instrumental accompaniment to vocal pieces
     * of pop music with simple, happy-go-lucky, and often sentimental lyrics.
     * It is prevalent in Central, Northern and Southeast Europe (in particular Germany,
     * Austria, Albania, Bulgaria, Finland, Slovenia, Serbia, Croatia, Poland, Hungary,
     * Romania, Switzerland, Scandinavia, and the Baltic states), and also
     * (to a lesser extent) in France, Belgium, the Netherlands, and the UK.
     * In the United States it is also known as 'entertainer music' or 'German hit mix'.
     *
     * @see <a href="https://en.wikipedia.org/wiki/Schlager_music">Wikipedia</a>
     */
    SCHLAGER,
    /**
     * Jazz is a music genre that originated in the African-American communities of
     * New Orleans, Louisiana, United States, in the late 19th and early 20th centuries,
     * with its roots in blues and ragtime. Since the 1920s Jazz Age, it has
     * been recognized as a major form of musical expression in traditional and popular
     * music, linked by the common bonds of African-American and European-American
     * musical parentage. Jazz is characterized by swing and blue notes,
     * complex chords, call and response vocals, polyrhythms and improvisation.
     * Jazz has roots in West African cultural and musical expression, and in
     * African-American music traditions.
     *
     * @see <a href="https://en.wikipedia.org/wiki/Jazz">Wikipedia</a>
     */
    JAZZ,
    /**
     * Blues is a music genre and musical form which was originated in the
     * Deep South of the United States around the 1860s by African-Americans
     * from roots in African musical traditions, African-American work songs,
     * and spirituals. Blues incorporated spirituals, work songs, field hollers,
     * shouts, chants, and rhymed simple narrative ballads. The blues form, ubiquitous
     * in jazz, rhythm and blues and rock and roll, is characterized by the
     * call-and-response pattern, the blues scale and specific chord progressions,
     * of which the twelve-bar blues is the most common. Blue notes (or "worried notes"),
     * usually thirds, fifths or sevenths flattened in pitch are also an essential
     * part of the sound. Blues shuffles or walking bass reinforce the trance-like
     * rhythm and form a repetitive effect known as the groove.
     *
     * @see <a href="https://en.wikipedia.org/wiki/Blues">Wikipedia</a>
     */
    BLUES,
    /**
     * Classical music is art music produced or rooted in the traditions of Western
     * culture, including both liturgical (religious) and secular music.
     * While a more precise term is also used to refer to the period from 1750 to 1820
     * (the Classical period), this article is about the broad span of time from before
     * the 6th century AD to the present day, which includes the Classical period and
     * various other periods. The central norms of this tradition became codified
     * between 1650 and 1900, which is known as the common-practice period.
     *
     * @see <a href="https://en.wikipedia.org/wiki/Classical_music">Wikipedia</a>
     */
    CLASSICAL,
    /**
     * Reggae (/ˈrɛɡeɪ/) is a music genre that originated in Jamaica in the late 1960s.
     * The term also denotes the modern popular music of Jamaica and its diaspora.
     * A 1968 single by Toots and the Maytals, "Do the Reggay" was the first popular
     * song to use the word "reggae", effectively naming the genre and introducing it
     * to a global audience. While sometimes used in a broad sense to refer to
     * most types of popular Jamaican dance music, the term reggae more properly denotes
     * a particular music style that was strongly influenced by traditional mento as well
     * as American jazz and rhythm and blues, especially the New Orleans R&B practiced
     * by Fats Domino and Allen Toussaint, and evolved out of the earlier genres ska
     * and rocksteady. Reggae usually relates news, social gossip, and political
     * commentary. Reggae spread into a commercialized jazz field, being known first
     * as "rudie blues", then "ska", later "blue beat", and "rock steady".
     * It is instantly recognizable from the counterpoint between the bass and drum
     * downbeat and the offbeat rhythm section. The immediate origins of reggae were
     * in ska and rocksteady; from the latter, reggae took over the use of the bass as
     * a percussion instrument.
     *
     * @see <a href="https://en.wikipedia.org/wiki/Reggae">Wikipedia</a>
     */
    REGGAE
}
